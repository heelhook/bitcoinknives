BTCPAY_CERTIFICATE_FILE_PATH = "#{Rails.root}/config/btcpay_certificate.pem"
BTCPAY_NOTIFICATION_EMAIL = ENV['BTCPAY_NOTIFICATION_EMAIL']
BTCPAY_URL = ENV['BTCPAY_URL']
